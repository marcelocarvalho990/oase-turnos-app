export function langFromCookie(cookieValue: string | undefined): Lang {
  if (cookieValue === 'de') return 'de'
  if (cookieValue === 'en') return 'en'
  if (cookieValue === 'fr') return 'fr'
  if (cookieValue === 'it') return 'it'
  return 'pt'
}
